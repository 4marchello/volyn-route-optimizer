    "def dijkstra(graph, start_node_index):\n",
    "    N = len(graph)\n",
    "    distances = [INF] * N\n",
    "    visited = [False] * N\n",
    "    predecessors = [-1] * N \n",
    "    distances[start_node_index] = 0\n",
    "\n",
    "    for _ in range(N):\n",
    "        min_dist = INF\n",
    "        u = -1 \n",
