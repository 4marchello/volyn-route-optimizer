    "                min_dist = distances[i]\n",
    "                u = i\n",
    "        \n",
    "        if u == -1:\n",
    "            break\n",
    "            \n",
    "        visited[u] = True\n",
    "        \n",
    "        for v in range(N):\n",
    "            weight = graph[u][v]\n",
    "            if weight != INF: \n",
    "                new_dist = distances[u] + weight\n",
    "                if new_dist < distances[v]:\n",
    "                    distances[v] = new_dist\n",
    "                    predecessors[v] = u \n",
    "                    \n",
